// sexpr builds a tree-sitter-style S-expression from a parse tree,
// including only named nodes.
func sexpr(n *gotreesitter.Node, lang *gotreesitter.Language) string {
	if n == nil {
		return ""
	}
	name := n.Type(lang)
	if !n.IsNamed() {
		return ""
	}
	if n.ChildCount() == 0 {
		return "(" + name + ")"
	}
	var parts []string
	for i := 0; i < n.ChildCount(); i++ {
		s := sexpr(n.Child(i), lang)
		if s != "" {
			parts = append(parts, s)
		}
	}
	if len(parts) == 0 {
		return "(" + name + ")"
	}
	return "(" + name + " " + strings.Join(parts, " ") + ")"
}

