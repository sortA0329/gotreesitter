func TestCorrectnessSnapshots(t *testing.T) {
	entries := AllLanguages()
	t.Cleanup(func() { PurgeEmbeddedLanguageCache() })
	entryByName := make(map[string]LangEntry, len(entries))
	for _, e := range entries {
		entryByName[e.Name] = e
	}

	for name, golden := range correctnessGoldens {
		t.Run(name, func(t *testing.T) {
			entry, ok := entryByName[name]
			if !ok {
				t.Fatalf("language %q not registered", name)
			}
			// Snapshot tests should always load a fresh embedded language so
			// preceding package tests cannot leak cached runtime mutations into
			// the golden parse surface.
			UnloadEmbeddedLanguage(entry.Name + ".bin")
			t.Cleanup(func() { UnloadEmbeddedLanguage(entry.Name + ".bin") })
			lang := entry.Language()
			report := EvaluateParseSupport(entry, lang)
			sample := ParseSmokeSample(name)
			parser := gotreesitter.NewParser(lang)
			src := []byte(sample)

			var tree *gotreesitter.Tree
			var parseErr error
			switch report.Backend {
			case ParseBackendTokenSource:
				ts := entry.TokenSourceFactory(src, lang)
				tree, parseErr = parser.ParseWithTokenSource(src, ts)
			case ParseBackendDFA, ParseBackendDFAPartial:
				tree, parseErr = parser.Parse(src)
			default:
				t.Fatalf("unsupported backend %q", report.Backend)
			}
			if parseErr != nil {
				t.Fatalf("parse failed: %v", parseErr)
			}

			if tree == nil || tree.RootNode() == nil {
				t.Fatal("parse returned nil root")
			}
			defer tree.Release()

			got := sexpr(tree.RootNode(), lang)
			if got != golden {
				t.Errorf("S-expression mismatch\ngot:  %s\nwant: %s", got, golden)
			}
		})
	}
}
