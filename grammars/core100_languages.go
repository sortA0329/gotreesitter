// Core100LanguageNames returns a copy of the compile-time Core100 grammar set.
func Core100LanguageNames() []string {
	out := make([]string, len(core100LanguageNames))
	copy(out, core100LanguageNames)
	return out
}

func languageSetFromNames(names []string) map[string]struct{} {
	set := make(map[string]struct{}, len(names))
	for _, name := range names {
		set[name] = struct{}{}
	}
	return set
}
